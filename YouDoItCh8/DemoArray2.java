/*
Purpose: This Java program demonstrates the use of an array to store and display salary values. 
*/

public class DemoArray2 {
    public static void main(String[] args) {
        // Initializing the array directly during its declaration
        double[] salaries = {16.25, 17.55, 18.25, 19.85};

        // Displaying the salaries
        System.out.println("Salaries one by one are:");
        System.out.println(salaries[0]);
        System.out.println(salaries[1]);
        System.out.println(salaries[2]);
        System.out.println(salaries[3]);
    }
}
